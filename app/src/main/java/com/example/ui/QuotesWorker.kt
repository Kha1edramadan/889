            "الجفاف بيتنكر في صورة تعب وصداع — اشرب أولاً",
            "لو حاسس بتعب من غير سبب — اشرب مياه"
        )
    }

    override fun doWork(): Result {
        createChannel()
        val quote = getNextQuote()
        showNotification(quote)
        return Result.success()
    }

    // ─── Anti-repeat: مش هيكرر نفس الاقتباس قبل ANTI_REPEAT اقتباس ──────────

    private fun getNextQuote(): String {
        val prefs = applicationContext.getSharedPreferences(PREFS_NAME, Context.MODE_PRIVATE)
        val shownJson = prefs.getString(KEY_SHOWN, "[]") ?: "[]"

        val shownSet = try {
            val arr = JSONArray(shownJson)
            (0 until arr.length()).map { arr.getInt(it) }.toMutableSet()
        } catch (_: Exception) { mutableSetOf() }

        // لو اتملى الـ buffer ابدأ من الأول
        if (shownSet.size >= ANTI_REPEAT) shownSet.clear()

        val available = ALL_QUOTES.indices.filter { it !in shownSet }
        val picked = available.random()
        shownSet.add(picked)

        prefs.edit()
            .putString(KEY_SHOWN, JSONArray().apply { shownSet.forEach { put(it) } }.toString())
            .apply()

        return ALL_QUOTES[picked]
    }

    // ─── Notification ─────────────────────────────────────────────────────────

    private fun createChannel() {
        val mgr = applicationContext.getSystemService(NotificationManager::class.java)
        if (mgr.getNotificationChannel(CHANNEL_ID) != null) return

        val channel = NotificationChannel(
            CHANNEL_ID,
            "اقتباسات وتذكيرات",
            NotificationManager.IMPORTANCE_HIGH
        ).apply {
            description = "رسائل يومية تحفيزية وتأملية"
            enableVibration(false)
            setBypassDnd(true)          // بيوصل حتى في وضع الصامت
        }
        mgr.createNotificationChannel(channel)
    }

    private fun showNotification(rawQuote: String) {
        // فصل النص عن المصدر
        val parts   = rawQuote.split(" | ")
        val body    = parts.getOrElse(0) { rawQuote }
        val source  = parts.getOrElse(1) { "" }
        val title   = if (source.isNotEmpty()) source else "IGYM"

        val tapIntent = PendingIntent.getActivity(
            applicationContext, 0,
            Intent(applicationContext, MainActivity::class.java).apply {
                flags = Intent.FLAG_ACTIVITY_SINGLE_TOP
            },
            PendingIntent.FLAG_UPDATE_CURRENT or PendingIntent.FLAG_IMMUTABLE
        )

        val notif = NotificationCompat.Builder(applicationContext, CHANNEL_ID)
            .setSmallIcon(R.drawable.ic_igym_notification)
            .setContentTitle(title)
            .setContentText(body)
            .setStyle(NotificationCompat.BigTextStyle().bigText(body))
            .setContentIntent(tapIntent)
            .setAutoCancel(true)
            .setPriority(NotificationCompat.PRIORITY_HIGH)
            .build()

        applicationContext
            .getSystemService(NotificationManager::class.java)
            .notify(NOTIF_ID, notif)
    }
